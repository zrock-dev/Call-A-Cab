package View.Utils;

/**
 * This is the color class.
 *
 * This class contains all the colors that will
 * be used for the messages that the code needs.
 */
public class Colors {

    /**
     *This method contains the colors.
     *
     * With this static method we can call the color
     * by its name.
     */
    public static final String RED = "\u001B[31m",
            GREEN = "\u001B[32m",
            BLUE = "\u001B[34m",
            PURPLE = "\u001B[35m",
            CYAN = "\u001B[36m",
            WHITE = "\u001B[37m",
            RESET="\u001B[0m",
            YELLOW_BOLD = "\033[1;33m";
}

